using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.HttpsPolicy;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Hosting;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Hangfire;//Sonradan eklendi
using HangfireApp.Web.Services;

namespace HangfireApp.Web
{
    public class Startup
    {
        public Startup(IConfiguration configuration)
        {
            Configuration = configuration;
        }

        public IConfiguration Configuration { get; }

        public void ConfigureServices(IServiceCollection services)
        {
            services.AddScoped<IEmailSender, MailSender>();//IEmailSender Gördüðünde MailSender nesnesi üretiyoruz
            services.AddHangfire(con => con.UseSqlServerStorage(Configuration.GetConnectionString("HangFireConStr")));//Hangfire yolunu verdik. Joblar buraya kaydolacak uygulama ilk kez ayaða kalktýðýnda tablolar veritabanýnda oluþur
            services.AddHangfireServer();//Hangfire çalýþtýrdýk


            services.AddControllersWithViews();
        }

        public void Configure(IApplicationBuilder app, IWebHostEnvironment env)
        {
            if (env.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
            }
            else
            {
                app.UseExceptionHandler("/Home/Error");
                app.UseHsts();
            }
            app.UseHttpsRedirection();
            app.UseStaticFiles();

            app.UseHangfireDashboard("/hangfire");//dashboard tanýmladýk. Siteadý/verdiðimiz ad burada mesela siteadý/hangfire diyince dashboard görünecek Routing öncesinde tanýmlanmalý burada joblar,tekrar eden iþlemler,Yinelenen joblar ve Server adý var

            app.UseRouting();

            app.UseAuthorization();

            app.UseEndpoints(endpoints =>
            {
                endpoints.MapControllerRoute(
                    name: "default",
                    pattern: "{controller=Home}/{action=Index}/{id?}");
            });
        }
    }
}
